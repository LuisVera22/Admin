    listServicio(){
        this.tblListServicio = $('#tblListServicio').DataTable({
            ordering: false,
            stateSave: true,
            language: {
                url: 'https://cdn.datatables.net/plug-ins/1.10.25/i18n/Spanish.json',
                paginate: {
                    previous: '<',
                    next: '>'
                }
            },
            ajax: {
                url: "json/ajax_producto/listarProducto.php",
                type: "POST",
                data: { listar: "toService", product: "services" },
                dataSrc: '',                
            },
            responsive: true,
            columnDefs: [
                { responsivePriority: 1, targets: 0 },
                { responsivePriority: 2, targets: -1 }
            ],
            columns: [
                { 'data': 'description' },
                { 'data': 'price' },
                { 'data': 'productsxstore.store.address' },
                { 'data': 'productsxstore.store.headquarters.description' },                
                { 'data': 'enabled' },
                { 'data': 'acciones' },
            ]
        });
    }
